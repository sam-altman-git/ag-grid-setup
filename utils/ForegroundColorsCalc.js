const DEFAULT_BG_COLOR = { r: 0, g: 0, b: 0 };
const BRIGHT_FG_COLOR_CODE = "#ffffff";
const DARK_FG_COLOR_CODE = "#000000";

export const hex_to_rgb = (hex) => {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
  return result
    ? {
        r: parseInt(result[1], 16),
        g: parseInt(result[2], 16),
        b: parseInt(result[3], 16),
      }
    : null;
};
export const hex_inverse_bw = (rgb) => {
  const luminance = 0.2126 * rgb["r"] + 0.7152 * rgb["g"] + 0.0722 * rgb["b"];
  return luminance < 140 ? BRIGHT_FG_COLOR_CODE : DARK_FG_COLOR_CODE;
};

