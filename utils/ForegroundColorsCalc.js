export const getForegroundColor = (backgroundColor) => {
  const bgRgb = colorMap[backgroundColor]?.rgb || DEFAULT_BG_COLOR;
  const foregroundColor = hex_inverse_bw(bgRgb);
  return foregroundColor;
};

export const getForegroundColorByHex = (hexBgColorCode) => {
  const bgRgb = hex_to_rgb(hexBgColorCode);
  const foregroundColor = hex_inverse_bw(bgRgb);
  return foregroundColor;
};
